      <p><i class="bi bi-arrow-right-circle icon-green"></i>Incorporar o aspecto ético aos marcos de referências administrativas para garantir práticas de administração congruentes com os valores e princípios do serviço público.</p>
      <p><i class="bi bi-arrow-right-circle icon-green"></i> Combinar, criteriosamente, os aspectos ideais e regulamentares dos sistemas de condução da ética.</p>
      <p><i class="bi bi-arrow-right-circle icon-green"></i> Avaliar os efeitos das reformas da administração pública na conduta ética do serviço civil.</p>
      <p><i class="bi bi-arrow-right-circle icon-green"></i> Utilizar os princípios de condução da ética no serviço público para garantir altos padrões de conduta.</p>

