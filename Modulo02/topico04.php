      <h3 class="text-secondary m-3">Rede de Instituições de Combate à Corrupção e Resgate da Ética Pública</h3>
     <hr>
     <p>Nos dias 15 e 16 de junho de 1998, o Centro Latino-americano de <a href="#" data-toggle="tooltip" data-placement="top" title="Centro Latino-americano de Administração para o Desenvolvimento">Centro Latino-americano de Administração para o Desenvolvimento<i class="fa fa-search-plus"></i></a>, com o apoio da <a href="#" data-toggle="tooltip" data-placement="top" title="AECID - Agencia Española de Cooperación Internacional para el Desarrollo">Agência Espanhola de Cooperação Internacional para o Desenvolvimento<i class="fa fa-search-plus"></i></a>, realizou em Santa Cruz de la Sierra, na Bolívia, um Fórum Ibero-americano com o propósito de: examinar as formas mais proeminentes e as consequências sociais da corrupção, e de explorar as formas e métodos que atualmente têm sido adotados no mundo para combatê-la e, dessa maneira, buscar reduzir sua presença e atividade na região. Um dos resultados desse fórum foi a criação da Rede de Instituições de Combate à Corrupção e Resgate da Ética Pública – RICOREP.</p>

     <p>A RICOREP reúne diversas instituições governamentais e não-governamentais (instituições de investigação com sede nos Estados Unidos e na América Latina), que possuem as funções de combater a corrupção em todas as suas manifestações e de resgatar os princípios éticos no exercício da função pública. Essa rede está formada por pessoas que desenvolvem estudos e investigações sobre essa temática. Entre os seus objetivos, incluem-se: o intercâmbio de informações sobre a estrutura e organização de cada uma das instituições referidas e as normas vigentes em matéria de enfrentamento da corrupção. Entre outras atividades, promove eventos periodicamente que estejam relacionados a essa matéria, realiza atividades de formação, investigação, consultoria técnica e difusão de publicações especializadas.</p>

     <p>São considerados “Membros Plenos” da RICOREP: as instituições cuja missão e objetivos estejam referidos à luta contra qualquer tipo de corrupção e ao resgate da ética pública. São considerados “Membros Pessoais”: os professores, pesquisadores, assessores e consultores sobre o tema em questão. A Secretaria Geral da CLAD é membro de pleno direito da RICOREP, em seu caráter de instância coordenadora das redes do Centro. Os países membros que integram a Rede são: Argentina, Bolívia, Brasil, Chile, Colômbia, Costa Rica, Cuba, Equador, El Salvador, Espanha, Estados Unidos, Guatemala, Honduras, Índia, México, Nicarágua, Panamá, Paraguai, Peru, Portugal, Porto Rico, República Dominicana, Uruguai e Venezuela.</p>

