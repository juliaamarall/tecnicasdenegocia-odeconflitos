      <h3 class="text-secondary m-3">Iniciativa Interamericana de Capital Social, Ética e Desenvolvimento</h3>
     <hr>
     <p>Esse projeto liderado pelo Banco Interamericano de Desenvolvimento (BID) é fruto de um evento ocorrido em Honduras, entre os dias 1 e 3 de setembro de 2001. Tal evento denominou-se “Encontro Internacional Sobre Ética e Desenvolvimento: Os Novos Desafios” e despertou o interesse nos setores da sociedade, atraindo mais de 400 participantes. Na organização do evento participaram, conjuntamente, além do BID, os governos de Honduras e da Noruega. Durante esse encontro se reconheceu que existe uma “fome ética no mundo” e, portanto, a necessidade de estimular valores como instrumento para o desenvolvimento dos povos. Dentre as conclusões do evento, afirmou-se que, enquanto se mantiver a dinâmica do individualismo possessivo, a velocidade da acumulação, cada vez maior, fará aumentar a exclusão em nível de país e em nível mundial. Não é possível haver desenvolvimento para todos sem ética e solidariedade. Também se afirmou que os valores éticos básicos de nossa civilização, tais como a responsabilidade de uns com os outros, a sensibilidade diante da pobreza, o respeito profundo pela dignidade do pobre, a sensação de urgência frente aos danos irreversíveis, que a pobreza pode causar e auxiliar o outro de modo tal que não venha mais necessitar de ajudar, deveriam orientar o desenho de políticas e esforços pelo desenvolvimento.</p>

