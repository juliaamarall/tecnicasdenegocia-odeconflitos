      <div class="text-left mt-3" id="sub-02">
      <h3 class="fw-semibold text-secondary">Ética nas Organizações</h3>
      <hr>
      <p>A ética normativa tem como função precípua fazer recomendações e formular normas e prescrições morais a partir dos conceitos de liberdade, necessidade, valor, consciência e sociabilidade. Nessa perspectiva, a ética transcende a esfera filosófica do aspecto subjetivo do comportamento humano e se utiliza do conhecimento objetivo das estruturas sociais, das relações e das instituições para explicar a inter-relação entre moral e sociedade. Os atos morais são condicionados pela sociedade, mas o comportamento moral requer que o indivíduo se conscientize dos fatores sociais que o influenciam para que, assim, possa ser responsabilizado pelas próprias decisões.</p>
      <p>Mas, na medida em que as organizações se institucionalizam, a ética, por meio de códigos deontológicos e implementação de departamentos ou comissões de ética, cria um aparelhamento para produzir a ética com o objetivo de disciplinar a conduta do empregado e constituir um instrumento de punição rápida às transgressões de conduta. Exacerba-se, assim, a característica de controle social da ética, que, em princípio, existe em função da emancipação do sujeito e da percepção de alteridade expressa pela responsabilidade social e pelo exercício da cidadania.</p>
      <p>Assim, os Códigos de Ética surgem nas organizações com o objetivo de disciplinar a conduta do empregado e constituir instrumento de punição rápida às transgressões de conduta. Nos anos noventa, os empregados são estimulados a levantar questões sobre condutas inadequadas dos profissionais das organizações e o Código de Ética mantém o objetivo inicial, mas incorpora a necessidade de vinculação à cultura organizacional. Entre os anos 1990 e 2000, a utilização de códigos de conduta por parte de médias e grandes empresas aumenta de forma substancial no Reino Unido e nos Estados Unidos e, em menor escala, em outros países da Europa. O código tem o objetivo de estabelecer os parâmetros da organização sobre a conduta ética dos membros e evidencia a expectativa da empresa de reconhecimento por parte dos funcionários sobre as dimensões éticas do comportamento organizacional.</p>
      <p>O Código de Ética é um instrumento formal que delineia a cultura, a política e os valores organizacionais, e orienta o comportamento corporativo. Para a execução de um programa de ética, o código é a ferramenta fundamental. A eficiência do código depende do comprometimento dos dirigentes quanto aos valores nele expressos e da participação de todos os empregados no processo de elaboração. Mas elaborar, formalizar e impor o código de ética sem ações específicas de promoção, avaliação e reformulação pode levá-lo ao esquecimento no curto prazo, com o risco de transformar-se em fonte de ressentimentos entre os membros da organização. Além disso, as violações ao código de ética, tanto de níveis hierárquicos superiores quanto inferiores, sem a oportuna reação da empresa, causam a perda de credibilidade dos programas de gestão da ética e inviabilizam a aplicação do código.</p>
      <p>A ética também constitui elemento essencial para o desenvolvimento econômico e deve ser entendida como uma prática social que influência os planos estratégicos de empresas e governos pressupondo a adoção de princípios de governança, os quais mensuram a capacidade para a gestão de condutas antiéticas e se consubstanciam em equidade, transparência, ética, prestação de contas.</p>
      <p>Tanto que, em 1998, o Comitê de Administração Pública da OCDE publicou recomendação sobre princípios de gestão da ética no serviço público, demonstrando que estabelecer alto padrão de conduta no serviço público tornou-se tema crítico para os países-membros da Organização. Reformas administrativas que envolvam esforços de responsabilidade e discrição dos servidores públicos, pressões orçamentárias e novas formas de oferecer os serviços públicos constituem desafios aos valores tradicionais da esfera pública. Assim, como indicamos, “o processo de prevenção de transgressões éticas é tão complexo quanto o fenômeno de transgressão em si”. Por isso, uma rede integrada de mecanismos é necessária para o sucesso da implementação de um sistema de gestão da ética.</p>
      <p>A configuração e os métodos de funcionamento da Organização diferenciam-se daqueles empregados nas organizações internacionais especializadas, pois as ações são fundamentadas na persuasão. A rede de mecanismos apresenta características de think-tank, mas também de tribunal, foro de coordenação e aconselhamento em diferentes áreas. De natureza exploratória e multidisciplinar, propõe-se a apresentar respostas pioneiras para os desafios da agenda internacional. Para tal, utiliza-se da troca de informações e consultas, em um processo de construção de consenso por meio do conhecimento, ambiente propício à coordenação intergovernamental. O trabalho é realizado em comitês temáticos, em que não se excluem as funções fiscalizadora e normativa. O processo decisório é dominado pelos grandes países industrializados. Os documentos finais gerados nos comitês, as Recomendações, embora não obriguem, contêm elevado grau de cobrança, uma vez que os países-membros realizam acompanhamento periódico para verificar a adoção das medidas. Essa prática reforça o caráter de constrangimento, de obrigação moral, que conduz ao cumprimento. As atividades priorizam, portanto, a flexibilidade e a persuasão, por meio de comprometimento e monitoramento.</p>
      
