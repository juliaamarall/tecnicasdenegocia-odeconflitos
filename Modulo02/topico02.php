<p>Às vezes, a questão é pequena e seu impacto também, mas a estratégia usada para resolver o problema é demorada, e então gasta-se tempo (que é dinheiro!) dos colaboradores. Assim, para evitar esse desperdício de tempo e dinheiro, deve-se avaliar o impacto adequadamente.</p>
