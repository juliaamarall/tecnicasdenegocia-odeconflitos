             <p>Isso deve ser feito o mais rápido possível, porque se a questão não for vista ou levada em consideração dentro de um período razoável, o conflito aumenta gerando prejuízos para o time. Afinal, conflitos inibem a produtividade e geram distrações — isso afeta o trabalho, o ritmo e a organização como um todo.</p>
