                <p>É comum que as partes façam relatos longos, carregados de juízos de valor, agressividade e outros elementos que prejudicam o entendimento do cerne da questão.</p>

                <p>Por exemplo, um trabalhador não dirá, pura e simplesmente, que gostaria de um pedido de desculpas da empregadora, mas que a empresa é incompetente, incorreta, descuidada e coisas do mesmo gênero.</p>

                <p>Sendo assim, o mediador precisa compreender a essência do que é dito e modificar a maneira como a ideia é exposta. Isso pode ocorrer por paráfrases que sintetizem o conteúdo da fala ou exponham a questão livre de emoções.</p>

                <p>Essa técnica também pode ser chamada de recontextualização, uma vez que o conteúdo é retirado de circunstâncias negativas e recolocado em uma linguagem positiva e prospectiva. Por exemplo, a demanda acima poderia ser traduzida como uma solicitação de reconhecimento da conduta praticada pela organização</p>

                
